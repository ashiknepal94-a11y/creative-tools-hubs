// Generate Emoji Art
generateEmojiArtBtn.addEventListener('click', () => {
    const type = emojiArtType.value;
    const size = emojiArtSize.value;
    
    const art = emojiArtTemplates[type][size];
    
    if (art) {
        emojiArtResult.textContent = art.join('\n');
        copyEmojiArtBtn.style.display = 'block';
        
        showNotification('Emoji art generated successfully!', 'success');
    }
});

// Copy Emoji Art
copyEmojiArtBtn.addEventListener('click', () => {
    const text = emojiArtResult.textContent;
    copyToClipboard(text);
});