// Generate ASCII Art
generateAsciiArtBtn.addEventListener('click', () => {
    const text = asciiInput.value.trim().toUpperCase();
    const font = asciiFont.value;
    
    if (!text) {
        showNotification('Please enter text for ASCII art', 'error');
        return;
    }
    
    const fontData = asciiFonts[font];
    
    if (!fontData) {
        showNotification('Invalid font selected', 'error');
        return;
    }
    
    // Generate ASCII art
    let asciiArt = '';
    const height = fontData['A'].length;
    
    for (let row = 0; row < height; row++) {
        for (let i = 0; i < text.length; i++) {
            const char = text[i];
            const charData = fontData[char] || fontData[' '];
            
            if (charData && charData[row]) {
                asciiArt += charData[row];
            } else {
                // Add space if character not found
                asciiArt += ' '.repeat(fontData['A'][0].length);
            }
        }
        asciiArt += '\n';
    }
    
    // Display ASCII art
    asciiArtResult.textContent = asciiArt;
    copyAsciiArtBtn.style.display = 'block';
    
    showNotification('ASCII art generated successfully!', 'success');
});

// Copy ASCII Art
copyAsciiArtBtn.addEventListener('click', () => {
    const text = asciiArtResult.textContent;
    copyToClipboard(text);
});